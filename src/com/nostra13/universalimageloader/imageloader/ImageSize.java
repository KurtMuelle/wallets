package com.nostra13.universalimageloader.imageloader;

/**
 * Present width and height values
 * 
 * @author Sergey Tarasevich (nostra13[at]gmail[dot]com)
 */
class ImageSize {
	int width;
	int height;

	public ImageSize(int width, int height) {
		this.width = width;
		this.height = height;
	}
}
